class ReLU(Activation):
    def __init__(self):
        super().__init__()

    

    def forward(self, Z: np.ndarray) -> np.ndarray:
        """Forward pass for relu activation:
        f(z) = z if z >= 0
               0 otherwise
        
        Parameters
        ----------
        Z  input pre-activations (any shape)

        Returns
        -------
        f(z) as described above applied elementwise to `Z`
        """

        mask_mtx = Z>0
        return Z*mask_mtx


    def backward(self, Z: np.ndarray, dY: np.ndarray) -> np.ndarray:
        """Backward pass for relu activation.
        
        Parameters
        ----------
        Z   input to `forward` method
        dY  derivative of loss w.r.t. the output of this layer
            same shape as `Z`

        Returns
        -------
        derivative of loss w.r.t. input of this layer"""
        return dY*(Z > 0)


class SoftMax(Activation):
    def __init__(self):
        super().__init__()

    def forward(self, Z: np.ndarray) -> np.ndarray:
        """Forward pass for softmax activation.
        Hint: The naive implementation might not be numerically stable.
        
        Parameters
        ----------
        Z  input pre-activations (any shape)

        Returns
        -------
        f(z) as described above applied elementwise to `Z`
        """
        max_vals_per_row = np.amax(Z,axis=1).reshape(-1,1)

        max_mtx=np.tile(max_vals_per_row,(1,Z.shape[1]))

        exp_mtx = np.exp(Z - max_mtx)

        exp_sum_vec = np.sum(exp_mtx,axis=1)

        exp_sum_mtx = np.tile(exp_sum_vec.reshape(-1,1),(1,Z.shape[1]))

        softmax_res = exp_mtx / exp_sum_mtx
        
        return softmax_res

    def backward(self, Z: np.ndarray, dY: np.ndarray) -> np.ndarray:
        """Backward pass for softmax activation.
        
        Parameters
        ----------
        Z   input to `forward` method
        dY  derivative of loss w.r.t. the output of this layer
            same shape as `Z`

        Returns
        -------
        derivative of loss w.r.t. input of this layer
        """

        result = np.zeros_like(Z)


        d = Z.shape[1]
        p = Z.shape[0] # number of data points

        S = self.forward(Z)

        for i in range(p):  
            dY_row = dY[i,:].reshape(1,-1) # as row vector
            s = S[i,:].reshape(1,-1) # the current data point. loop over it

            s_js = np.tile(s.reshape(-1,1),(1,d)) 
            """ like
            s_js 
            [[0 0 0]
            [4 4 4]
            [9 9 9]]
            """

            s_is = np.tile(s.reshape(1,-1),(d,1))
            """like
            s_is 
            [[0 4 9]
            [0 4 9]
            [0 4 9]]
            """

            ident = np.eye(d)

            jacobian = np.multiply(s_js,(ident-s_is))

            result[i,:] = np.dot(dY_row,jacobian)
        return result


class ArcTan(Activation):
    def __init__(self):
        super().__init__()

    def forward(self, Z):
        return np.arctan(Z)

    def backward(self, Z, dY):
        return dY * 1 / (Z ** 2 + 1)
